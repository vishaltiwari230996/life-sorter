// Get tools for a specific domain
export const getToolsForDomain = (domain) => {
  const domainTools = productHuntTools[domain] || [];
  return [...domainTools, ...productHuntTools.common];
};

// Get top N tools for a domain
export const getTopToolsForDomain = (domain, limit = 4) => {
  const tools = getToolsForDomain(domain);
  // Sort by upvotes and return top N
  return tools.sort((a, b) => b.upvotes - a.upvotes).slice(0, limit);
};

// Filter tools by budget
export const filterToolsByBudget = (tools, budgetId) => {
  const budgetLimits = {
    'free': 0,
    'low': 2000,
    'medium': 10000,
    'high': Infinity
  };

  const limit = budgetLimits[budgetId] || Infinity;

  return tools.filter(tool => {
    // Extract numeric price (rough estimate)
    const priceMatch = tool.price.match(/₹([\d,]+)/);
    if (!priceMatch) return true; // If free or can't parse, include
    const price = parseInt(priceMatch[1].replace(',', ''));
    return price <= limit || tool.price.toLowerCase().includes('free');
  });
};
