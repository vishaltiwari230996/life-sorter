// Get apps for a specific domain (domain-specific + common)
export const getAppsForDomain = (domain) => {
  const domainApps = workspaceApps[domain] || [];
  return [...domainApps, ...workspaceApps.common];
};

// Get top N apps for a domain
export const getTopAppsForDomain = (domain, limit = 4) => {
  const apps = getAppsForDomain(domain);
  return apps.slice(0, limit);
};
